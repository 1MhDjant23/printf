#include "ft_printf.h"

size_t	ft_putnb(int x)
{
	size_t	i;
	char	digit;

	if (x == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	i = 0;
	if (x < 0)
	{
		i++;
		write(1, "-", 1);
		x *= -1;
	}
	if (x > 9)
		i += ft_putnb(x / 10);
	digit = '0' + (x % 10);
	i += write(1, &digit, 1);
	return (i);
}
